# Operators 

print( 3 + 4)
print( 3 - 4)
print( 3 * 4)
print( 3 / 4)
print( 3 % 4)
print( 3 // 4)  #this is a floor division, will round the number to an integer
print( 3 ** 4) #exponent
print("Hello " + "Python") #exponent

# Comparative Operators

print(3>4)
print(3<4)
print(3>=4)
print(3<=4)
print(3==4)
print(3!=4)

# Logical Operators

print(3>4 and "Hey python" > "python") # && in javascript
print(3>4 or "hey python" > "python") # || in javascript
print(not(3>4)) # !== in javascript



